SELECT 
    A.employee_id,
    A.first_name,
    A.last_name,
    A.email,
    A.phone_number,
    A.hire_date,
    A.job_id,
    B.JOB_TITLE,
    A.salary,
    A.commission_pct,
    A.manager_id,
    A.department_id,
    C.MANAGER_ID,
    C.FIRST_NAME AS MANAGER_FIST_NAME,
    C.LAST_NAME AS MANAGER_LAST_NAME,
    D.FIRST_NAME AS MANAGER2_FIRST_NAME,
    D.LAST_NAME AS MANAGER2_LAST_NAME,
    E.DEPARTMENT_NAME,
    E.LOCATION_ID,
    F.CITY,
    F.COUNTRY_ID,
    G.COUNTRY_NAME,
    G.REGION_ID,
    H.REGION_NAME
FROM employees A,
        JOBS B,
        EMPLOYEES C,
        EMPLOYEES D,
        DEPARTMENTS E,
        LOCATIONS F,
        COUNTRIES G,
        REGIONS H
WHERE A.JOB_ID = B.JOB_ID
AND   A.MANAGER_ID = C.EMPLOYEE_ID(+)
AND C.MANAGER_ID = D.EMPLOYEE_ID (+)
AND A.DEPARTMENT_ID = E.DEPARTMENT_ID(+)
AND E.LOCATION_ID = F.LOCATION_ID(+)
AND F.COUNTRY_ID = G.COUNTRY_ID(+)
AND G.REGION_ID = H.REGION_ID(+);



